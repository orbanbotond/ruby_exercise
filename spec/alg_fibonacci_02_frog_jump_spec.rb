def do_debug
  # debugging = true
  debugging = false
  puts yield if debugging
end

def generate_fibonacci(max)
  array = Array.new
  array << 0
  array << 1
  while array.last < max do
    array << array[-1] + array[-2]
  end
  array
end

def possibilities(a, pos, fib, goal, already_there)
  ret = []
  fib.each do |x|
    do_debug{"Trying #{x}"}
    if pos.first + x == a.size
      do_debug{"reached the end"}
      goal << pos.last + 1
      return ret
    end
    if pos.first + x > a.size ||
      a[pos.first + x] == 0 ||
      already_there[pos.first + x] == true
      do_debug{"already there:#{already_there[pos.first + x]}"}
      do_debug{"not reachable:#{a[pos.first + x] == 0}"}
      do_debug{"the jump is longer:#{pos.first + x > a.size}"}
      next
    end
    do_debug{"reaching pos:#{pos.first + x}"}
    ret << [pos.first + x, pos.last + 1]
  end
  do_debug{"reachable possibilities: #{ret}"}
  ret
end

def solution(a)
  fib = generate_fibonacci(a.size)
  fib.shift
  fib.shift
  do_debug{"fib:#{fib}"}
  do_debug{"a:#{a}"}

  already_there = {}
  queue = [[-1,0]]
  goal = []

  while queue.size != 0
    pos =  queue.shift
    do_debug{"q: #{queue} examining:#{pos}"}
    pos = possibilities(a, pos, fib, goal, already_there)
    return goal.first if goal.size != 0
    queue += pos
  end

  -1
end


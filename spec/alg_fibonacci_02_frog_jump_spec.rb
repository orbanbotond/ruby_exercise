def generate_fibonacci(max)
  array = Array.new
  array << 0
  array << 1
  while array.last < max do
    array << array[-1] + array[-2]
  end
  array
end

def possibilities(a, pos, fib, goal, already_there)
  ret = []
  pos.first.upto(a.size-1) do | x |
  # (a.size-1).downto(pos.first) do | x |
    if a[x] == 1 && fib.any?{|f| f == x + 1 - pos.first} 
      do_debug{"Can reach:#{x+1}"}
      if already_there[x+1]
        do_debug{'already reached... skipping'}
      else
        if x + 1 == a.size
          do_debug{'reached the end'}
          goal << pos.last + 1
        else
          already_there[x + 1] = true
          ret << [x + 1, pos.last + 1]
        end
      end
    end
  end
  do_debug{"reachable possibilities: #{ret}"}
  ret
end

def solution2(a)
  a << 1

  fib = generate_fibonacci(a.size)

  already_there = {}
  queue = [[0,0]]
  goal = []

  while queue.size != 0
    pos =  queue.shift
    do_debug{"q: #{queue} examining:#{pos}"}
    pos = possibilities(a, pos, fib, goal, already_there)
    return goal.first if goal.size != 0
    queue += pos
  end

  -1
end

